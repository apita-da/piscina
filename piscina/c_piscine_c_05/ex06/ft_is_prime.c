int		ft_is_prime(int nb)
{
	int cont;

	cont = 2;
	if (nb == 0 || nb == 1 || nb < 0)
		return (1);
	while (cont <= (nb / (2 * cont)))
	{
		if (nb % cont == 0)
			return (0);
		cont++;
	}
	return (1);
}
