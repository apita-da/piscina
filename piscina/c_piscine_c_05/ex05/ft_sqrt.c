int		ft_sqrt(int nb)
{
	int y;

	y = 0;
	if (nb < 1)
		return (0);
	while ((y * y) < nb && (y < 46340))
	{
		y++;
		if (y * y == nb)
			return (y);
	}
	return (0);
}
