char	*ft_strupcase(char *str)
{
	int cont;

	cont = 0;
	while (str[cont] != '\0')
	{
		if (str[cont] > 96 && str[cont] < 123)
			str[cont] -= 32;
		cont++;
	}
	return (str);
}
